numeros = [4, 5, 2, 130, 500]

# encontrar el numero mayor de una lista
numero_mas_alto = max(numeros)
print(f'El numero mas alto es: {numero_mas_alto}')

# encontrar el numero mayor de una lista
numero_mas_bajo = min(numeros)
print(f'El numero mas bajo es: {numero_mas_bajo}')

# redondear a 2 decimales (segundo argumento)
numero_redondo = round(12.3456, 2)
print(numero_redondo)

# retorna False si 0, vacio, False, ninguno
resultado_bool = bool(0)
print(resultado_bool)

# retorn True, si todos los valores son verdaderos
resultado_all = all(['asd', 5, True])
print(resultado_all)
